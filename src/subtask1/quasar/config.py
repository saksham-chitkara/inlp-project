# ─── Quasi-Symbolic Abstraction (QuaSAR) ─────────────────────────────────────
# When True, prepend a quasi-symbolic form to every input:
#   "<QuaSAR s2 formalisation> </s> <original text>"
#
# Primary method (technique 3): Llama-generated QuaSAR 4-step output.
#   The s2 (Formalisation) section is extracted and used as the abstract form.
#   Requires quasar_*_cache.json files (generated by quasar_generator.py).
#
# Fallback method (technique 1): spaCy rule-based X/Y/Z replacement.
#   Used if QuaSAR cache is not available for a given example.
USE_QUASI_SYMBOLIC = True
ABSTRACT_SEP = " </s> "   # XLM-RoBERTa's native separator token

# QuaSAR input mode:
#   "s2"      — s2 (formalisation) only  (short, ~10 tokens)
#   "s2_s3"   — s2 + s3 (formalisation + explanation, ~50 tokens)
#   "full"    — full 4-step QuaSAR output (s1+s2+s3+s4, ~100 tokens)
# Using more of the chain-of-thought gives XLM-RoBERTa richer signal.
QUASAR_MODE = "full"

# spaCy model for fallback neuro-symbolic extraction
SPACY_MODEL = "en_core_web_sm"

# ─── Activation Steering (CAA/K-CAST) ────────────────────────────────────────
# Adapted from Valentino et al. (2025) for encoder-only XLM-RoBERTa.
# The original paper targets decoder-only models (Llama, Gemma, Qwen); we
# apply the same contrastive steering principle to [CLS] layer activations.
#
# D+ / D- are BEHAVIOUR-BASED (per the paper):
#   D+ = examples where model predicts correctly (structure-based reasoning)
#   D- = examples where model predicts incorrectly (content-biased errors)
#
# Layers to hook: last quarter of XLM-RoBERTa-base (layers 9-11 of 12).
# Valentino et al. (2025) found that layers in the last third-quarter are
# most informative for validity vs. plausibility and best for steering.
STEERING_LAYERS = [11]  # Layer 11 has strongest content signal (||delta||=1.07)
STEERING_ALPHA = 1.0          # default scaling factor for the steering vector
STEERING_KNN = 10             # k for K-CAST nearest-neighbour lookup
USE_ACTIVATION_STEERING = True

# ─── Label Mapping ───────────────────────────────────────────────────────────
LABEL2ID = {False: 0, True: 1}
ID2LABEL = {0: False, 1: True}

# ─── Llama Model (alternative/supplementary classifier) ──────────────────
# Llama can be used (a) as an alternative classifier with LoRA fine-tuning,
# or (b) to generate quasi-symbolic abstractions (original QuaSAR approach).
# MUST use Instruct variant — base model doesn't follow structured prompts.
LLAMA_MODEL_NAME = "meta-llama/Llama-3.1-8B-Instruct"
USE_LORA = True           # LoRA for parameter-efficient fine-tuning
LORA_R = 16               # LoRA rank
LORA_ALPHA = 32           # LoRA scaling factor
LORA_DROPOUT = 0.1        # LoRA dropout
USE_4BIT = True           # QLoRA: 4-bit quantisation for memory efficiency
