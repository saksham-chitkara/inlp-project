"""
config.py
---------
Central configuration for Subtask 1: Syllogistic Reasoning in English.

Approach (from proposal):
  - Base model: XLM-RoBERTa (multilingual, strong cross-lingual transfer)
  - Neuro-Symbolic Integration + Quasi-Symbolic Abstraction: extract the
    logical form of an argument (e.g., "All X are Y") and provide it to
    the model alongside the original text, so that the model pays more
    attention to structure than to wording.
    (Adapted from Ranaldi, Valentino & Freitas, 2025 — QuaSAR, arXiv:2502.12616)
  - Activation Steering (Valentino et al., 2025 — arXiv:2505.12189): post
    fine-tuning, compute contrastive activation vectors using behaviour-based
    D+/D- sets (correct vs incorrect predictions) to steer the model away
    from content-biased reasoning at inference time.
"""

